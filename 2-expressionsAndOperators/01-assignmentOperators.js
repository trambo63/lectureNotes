// assignment operator
let r = 5;
//the single equal sign is the assignment operator

//addition assignment operator
r += 1; //  r = r + 1
console.log(r);

//Subtraction assignment operator
r -= 1; // r = r -1
console.log(r);

//multiplication assignment operator
r *= 1; // r = r * 1
console.log(r);

//division assignment operator
r /= 2;// r = r / 2
console.log(r);

//remainder (modulus) assingment operator
r %= 2; // r = r % 2
console.log(r);

//exponmential assignment operator
r **= 1 // r = r ** 1
console.log(r);
